    <h2>History</h2>
    <p>
        Join UO was originally created in April 2008 to fill a void in the UO freeshard community, providing free statistics for online clients to shard owners and players.
        Since this time, the JoinUO network has expanded to provide other services including voting, a searchable shard directory, and more shard statistics including multi year charting
        for clients, items, and mobiles in the "world".
    </p>
    <p>
        Our new site launched in December 2010 and provides integration with our forums, and the ability for shard owners to make multiple shard listings. The more advanced code on our new site
        is faster, and will be more reliable in eliminating vote fraud, multiple shard entries, and broken links and images. <a href="http://www.joinuo.com/forums/viewtopic.php?f=15&amp;t=624" title="New JoinUO Website Launched!">Click here for more info on the new website.</a>
